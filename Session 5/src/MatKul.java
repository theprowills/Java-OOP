
public class MatKul {
	private String nama;
	private String kode;

	public MatKul(String nama, String kode) {
		super();
		this.nama = nama;
		this.kode = kode;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public String getKode() {
		return kode;
	}

	public void setKode(String kode) {
		this.kode = kode;
	}

}
